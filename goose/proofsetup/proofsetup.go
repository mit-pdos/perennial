func importToRequireImport(coqPkgName string) string {
	i := strings.LastIndex(coqPkgName, ".")
	if i < 0 {
		panic(errors.Errorf("unexpected coqPkgName %s", coqPkgName))
	}
	return fmt.Sprintf("From %s Require Import %s.", coqPkgName[:i], coqPkgName[i+1:])
}

func New(pkg *packages.Package, verbose bool) ProofSetup {
	ffi := util.GetFfi(pkg)
	s := ProofSetup{
		PackageName: pkg.Name,
	}

	s.ProofPath = path.Join(
		"new/proof/",
		glang.ThisIsBadAndShouldBeDeprecatedGoPathToCoqPath(path.Dir(pkg.PkgPath)),
		pkg.Name+".v",
	)

	var importLines []string
	imports := pkgCoqImports(pkg)
	for _, coqName := range imports {
		importLines = append(importLines, importToRequireImport(coqName))
	}
	s.Imports = strings.Join(importLines, "\n")

	if ffi == "" {
		s.ContextVars = "Context `{hG: heapGS Σ, !ffi_semantics _ _} `{!globalsGS Σ} {go_ctx: GoContext}."
	} else {
		s.ContextVars = "Context `{hG: !heapGS Σ} `{!globalsGS Σ} {go_ctx: GoContext}."
	}

	names, wpsMap := packageWps(pkg, verbose)
	s.WpLemmaNames = names
	s.WpLemmasMap = wpsMap

	return s
}

func (pf ProofSetup) SkeletonFile() string {
	for _, name := range pf.WpLemmaNames {
		pf.WpLemmas = append(pf.WpLemmas, pf.WpLemmasMap[name])
	}

	tmpl := template.Must(template.New("proof.v").Option("missingkey=default").Parse(
		`{{.Imports}}

Section proof.
{{.ContextVars}}

#[global] Instance : IsPkgInit {{$.PackageName}} := define_is_pkg_init True%I.
#[global] Instance : GetIsPkgInitWf {{$.PackageName}} := build_get_is_pkg_init_wf.

{{ range $lemma := .WpLemmas -}}
(* Auto-generated by proof-setup *)
{{$lemma}}
Proof. Admitted.

{{ end -}}
End proof.
`))

	s := new(bytes.Buffer)
	err := tmpl.Execute(s, pf)
	if err != nil {
		panic(fmt.Errorf("internal error: executing template: %v", err))
	}
	return s.String()
}

func (pf ProofSetup) UpdateFile(fp string, verbose bool) {
	oldFile, err := os.ReadFile(fp)
	if err != nil {
		fmt.Fprintf(os.Stderr, "could not read file %v\n", fp)
		return
	}

	var newFile string
	foundLemmas := make(map[string]bool)

	newLemmas := func() {
		additional := ""
		for _, name := range pf.WpLemmaNames {
			if _, ok := foundLemmas[name]; !ok {
				additional += "(* Auto-generated by proof-setup *)\n"
				additional += pf.WpLemmasMap[name] + "\nProof. Admitted.\n\n"
				if verbose {
					fmt.Printf("added new lemma %s\n", name)
				}
			}
		}

		endProofIndex := strings.Index(newFile, "End proof.")
		if endProofIndex < 0 {
			fmt.Fprintf(os.Stderr, "could not find End proof. in file %s\n", fp)
			return
		}
		newFile = newFile[:endProofIndex] + additional + newFile[endProofIndex:]
	}

	sectionIndex := strings.Index(string(oldFile), "Section proof.")
	if sectionIndex < 0 {
		fmt.Fprintf(os.Stderr, "could not find Section proof. in file %s\n", fp)
		return
	}
	oldImports := string(oldFile[:sectionIndex])

	oldImportLines := strings.Split(oldImports, "\n")
	oldImportSet := make(map[string]bool)
	for _, line := range oldImportLines {
		oldImportSet[line] = true
	}
	newImportLines := strings.Split(pf.Imports, "\n")
	for _, line := range newImportLines {
		if !oldImportSet[line] {
			oldImportLines = append(oldImportLines, line)
		}
	}
	updatedImports := strings.Join(oldImportLines, "\n")
	newFile += updatedImports
	oldFile = oldFile[sectionIndex:]

	autoIndex := strings.Index(string(oldFile), "(* Auto-generated by proof-setup *)")
	if autoIndex < 0 {
		newFile += string(oldFile)
		newLemmas()
		err = os.WriteFile(fp, []byte(newFile), 0666)
		return
	}
	newFile += string(oldFile[:autoIndex])
	oldFile = oldFile[autoIndex:]

	for {
		autoIndex = 0

		endIndex := strings.Index(string(oldFile), "Proof.")
		if endIndex < 0 {
			// error
			fmt.Fprintf(os.Stderr, "could not find Proof. after auto-generated proof-setup comment in %s\n", fp)
			return
		}
		lemmaStart := strings.Index(string(oldFile), "Lemma ")
		if lemmaStart < 0 || lemmaStart > endIndex {
			// error
			fmt.Fprintf(os.Stderr, "could not find lemma after auto-generated proof-setup comment in %s\n", fp)
			return
		}
		lemmaEnd := strings.Index(string(oldFile)[lemmaStart+6:], " ")
		if lemmaEnd < 0 || lemmaEnd > endIndex-lemmaStart-6 {
			fmt.Fprintf(os.Stderr, "could not find end of lemma name after auto-generated proof-setup comment in %s\n", fp)
			return
		}
		lemmaName := string(oldFile)[lemmaStart+6 : lemmaStart+6+lemmaEnd]
		foundLemmas[lemmaName] = true

		var lemma string
		newLemma, ok := pf.WpLemmasMap[lemmaName]
		if ok {
			lemma = newLemma
		} else {
			lemma = string(oldFile[:endIndex])
		}

		newFile += "(* Auto-generated by proof-setup *)\n"
		newFile += lemma + "\n"

		if verbose {
			// find old lemma which is between lemmastart and endindex
			lemmaOld := string(oldFile[lemmaStart:endIndex])
			// fmt.Printf("comparing lemma %s\n", lemmaName)
			// fmt.Printf("old lemma:\n%s\n", lemmaOld)
			// fmt.Printf("new lemma:\n%s\n", lemma)

			// if the lemma stayed same print unchanged else print updated
			if lemmaOld == lemma+"\n" {
				fmt.Printf("unchanged lemma %s\n", lemmaName)
			} else {
				fmt.Printf("updated   lemma %s\n", lemmaName)
			}
		}

		nextAutoIndex := strings.Index(string(oldFile[endIndex:]), "(* Auto-generated by proof-setup *)")
		if nextAutoIndex < 0 {
			newFile += string(oldFile[endIndex:])
			break
		} else {
			newFile += string(oldFile[endIndex : endIndex+nextAutoIndex])
			oldFile = oldFile[endIndex+nextAutoIndex:]
		}
	}

	newLemmas()

	err = os.WriteFile(fp, []byte(newFile), 0666)
	// fmt.Print(newFile)
}
